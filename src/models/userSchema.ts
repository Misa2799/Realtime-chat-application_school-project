import { model, Schema } from "mongoose"; 

const userSchema = new Schema({
  idNumber: {
    type: Number,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  }
});

const User = model("User", userSchema);

export default User;
